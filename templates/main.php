<div class = "wiki"><p class = "zagwiki">Counter-Strike: Global Offensive, CS:GO </p><br><p class = "wikipedia-text">(с англ. — «Контрудар: глобальное наступление») — многопользовательская компьютерная игра, разработанная компаниями Valve
 и Hidden Path Entertainment. Последняя основная игра в серии игр Counter-Strike; как и все игры серии, она посвящена противостоянию террористов и подразделений специального назначения.
Выход на ПК под управлением Microsoft Windows, на компьютерах Mac и игровых приставках Xbox 360 и PlayStation 3, состоялся 21 августа 2012 года. В 2014 году была выпущена на Linux, а в 2016 году — стала
 доступна, в рамках программы обратной совместимости, на Xbox One. Первые новости о разработке игры появились вместе с официальным анонсом — 12 августа 2011 года. Слухи о том, что компания Valve, ответственная за серию игр Counter-Strike, 
 разрабатывает новую часть игры (последней на тот момент основной игрой серии, не считая отдельной версии, направленной на азиатскую аудиторию, была Counter-Strike: Source 2004 года) распространились несколькими днями ранее. 
 22 августа 2012 года (в 21:00 по Москве) состоялся релиз игры в Steam. В тот же день вышло первое обновление.
